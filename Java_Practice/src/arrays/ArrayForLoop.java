package arrays;

public class ArrayForLoop {

	public static void main(String[] args) {
		int s[]=new int[9];
		s[0]=134;
		s[1]=144;
		s[2]=342;
		s[3]=174;
		s[4]=136;
		s[5]=224;
		s[6]=664;
		s[7]=334;
		s[8]=114;
		
		for(int i=0;i<=9;i++)
		{
			System.out.println(s[i]);

		}
		
		}
}	


	


